// ==================== Learning Progress Manager ====================
class LearningProgressManager {
    constructor() {
        this.storageKey = 'gallerypia_learning_progress';
        this.progress = this.loadProgress();
    }

    loadProgress() {
        const stored = localStorage.getItem(this.storageKey);
        return stored ? JSON.parse(stored) : {};
    }

    saveProgress() {
        localStorage.setItem(this.storageKey, JSON.stringify(this.progress));
    }

    getCourseProgress(courseId) {
        return this.progress[courseId] || {
            startedAt: null,
            completedLessons: [],
            lastLessonId: null,
            quizScores: {},
            completed: false,
            completedAt: null,
            percentage: 0
        };
    }

    startCourse(courseId) {
        if (!this.progress[courseId]) {
            this.progress[courseId] = {
                startedAt: new Date().toISOString(),
                completedLessons: [],
                lastLessonId: null,
                quizScores: {},
                completed: false,
                completedAt: null,
                percentage: 0
            };
            this.saveProgress();
        }
    }

    completeLesson(courseId, lessonId, quizScore = null) {
        const courseProgress = this.getCourseProgress(courseId);
        
        if (!courseProgress.completedLessons.includes(lessonId)) {
            courseProgress.completedLessons.push(lessonId);
        }
        
        courseProgress.lastLessonId = lessonId;
        
        if (quizScore !== null) {
            courseProgress.quizScores[lessonId] = quizScore;
        }

        const course = COURSES_DATA[courseId];
        const totalLessons = course.lessonCount;
        const completedCount = courseProgress.completedLessons.length;
        courseProgress.percentage = Math.round((completedCount / totalLessons) * 100);

        if (completedCount === totalLessons && !courseProgress.completed) {
            courseProgress.completed = true;
            courseProgress.completedAt = new Date().toISOString();
        }

        this.progress[courseId] = courseProgress;
        this.saveProgress();

        return courseProgress;
    }

    getCertificates() {
        return Object.keys(this.progress)
            .filter(courseId => this.progress[courseId].completed)
            .map(courseId => ({
                courseId,
                course: COURSES_DATA[courseId],
                completedAt: this.progress[courseId].completedAt
            }));
    }
}

// ==================== Markdown Renderer ====================
class MarkdownRenderer {
    static render(markdown) {
        let html = markdown;

        // Headers
        html = html.replace(/^### (.*$)/gim, '<h3 class="text-xl font-bold text-white mt-8 mb-4">$1</h3>');
        html = html.replace(/^## (.*$)/gim, '<h2 class="text-2xl font-bold text-gradient mt-10 mb-5">$1</h2>');
        html = html.replace(/^# (.*$)/gim, '<h1 class="text-3xl font-bold text-gradient mt-12 mb-6">$1</h1>');

        // Bold
        html = html.replace(/\*\*(.*?)\*\*/g, '<strong class="text-white font-bold">$1</strong>');

        // Code blocks
        html = html.replace(/```([\s\S]*?)```/g, '<pre class="bg-gray-900 border border-purple-500/30 rounded-xl p-6 overflow-x-auto my-6"><code class="text-cyan-300 text-sm">$1</code></pre>');

        // Inline code
        html = html.replace(/`([^`]+)`/g, '<code class="bg-gray-900 text-cyan-400 px-2 py-1 rounded text-sm">$1</code>');

        // Tables
        html = this.renderTables(html);

        // Lists
        html = html.replace(/^\- (.*$)/gim, '<li class="ml-6 mb-2 text-gray-300">$1</li>');
        html = html.replace(/(<li.*<\/li>)/s, '<ul class="list-disc my-4">$1</ul>');

        // Blockquotes (highlights)
        html = html.replace(/^> \*\*💡 (.*?)\*\*/gim, '<div class="bg-gradient-to-r from-purple-900/30 to-cyan-900/30 border border-purple-500/30 rounded-xl p-6 my-6"><div class="flex items-start"><i class="fas fa-lightbulb text-yellow-400 text-2xl mr-4 mt-1"></i><div><h4 class="text-lg font-bold text-white mb-2">$1</h4>');
        html = html.replace(/^> /gim, '<p class="text-gray-300 leading-relaxed">');
        html = html.replace(/<\/div>$/gim, '</p></div></div>');

        // Paragraphs
        html = html.replace(/\n\n/g, '</p><p class="text-gray-300 leading-relaxed mb-4">');
        html = '<p class="text-gray-300 leading-relaxed mb-4">' + html + '</p>';

        // Horizontal rules
        html = html.replace(/^---$/gim, '<hr class="border-t border-gray-700 my-8">');

        return html;
    }

    static renderTables(html) {
        const tableRegex = /\|(.*)\|\n\|([-:\| ]+)\|\n((?:\|.*\|\n?)*)/g;
        
        return html.replace(tableRegex, (match, header, separator, rows) => {
            const headers = header.split('|').map(h => h.trim()).filter(h => h);
            const rowData = rows.trim().split('\n').map(row => 
                row.split('|').map(cell => cell.trim()).filter(cell => cell)
            );

            let tableHtml = '<div class="overflow-x-auto my-6"><table class="w-full border-collapse">';
            
            // Header
            tableHtml += '<thead><tr class="bg-gradient-to-r from-purple-900/50 to-cyan-900/50">';
            headers.forEach(h => {
                tableHtml += `<th class="border border-purple-500/30 px-4 py-3 text-left text-white font-bold">${h}</th>`;
            });
            tableHtml += '</tr></thead>';

            // Body
            tableHtml += '<tbody>';
            rowData.forEach((row, idx) => {
                const bgClass = idx % 2 === 0 ? 'bg-gray-900/30' : 'bg-gray-800/30';
                tableHtml += `<tr class="${bgClass}">`;
                row.forEach(cell => {
                    tableHtml += `<td class="border border-gray-700 px-4 py-3 text-gray-300">${cell}</td>`;
                });
                tableHtml += '</tr>';
            });
            tableHtml += '</tbody></table></div>';

            return tableHtml;
        });
    }
}

